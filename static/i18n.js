function normalizeLanguage(language) {
  return Object.hasOwn(I18N_MESSAGES, language) ? language : "zh";
}

function getStoredLanguage() {
  return normalizeLanguage(localStorage.getItem(I18N_STORAGE_KEY) || "zh");
}

function interpolate(template, values = {}) {
  return String(template).replace(/\{(\w+)}/g, (match, key) => {
    if (Object.hasOwn(values, key)) return values[key];
    return match;
  });
}

function getMessage(path, language = getStoredLanguage()) {
  const parts = path.split(".");
  let value = I18N_MESSAGES[normalizeLanguage(language)];
  for (const part of parts) {
    value = value?.[part];
  }
  return value;
}

window.AppI18n = {
  languages: ["zh", "en"],
  messages: I18N_MESSAGES,
  getLanguage: getStoredLanguage,
  setLanguage(language) {
    const normalized = normalizeLanguage(language);
    localStorage.setItem(I18N_STORAGE_KEY, normalized);
    return normalized;
  },
  t(path, values) {
    const value = getMessage(path) ?? getMessage(path, "zh") ?? path;
    return Array.isArray(value) ? value : interpolate(value, values);
  },
};
